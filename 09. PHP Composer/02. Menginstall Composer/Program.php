Menginstall Composer
https://getcomposer.org/download/
Download binary file composer, lalu masukan ke dalam path directory bin PHP, atau bisa gunakan direcotory terpisah,
namun perlu kita menambahkan ke PATH lagi jika menggunakan directory terpisah.
Setelah menginstall composer di PATH, kita bisa gunakan perintah berikut di terminal untuk mengecek versi composer.

Download: composer.phar (Linux/Mac/Windows)
Install: copy ke direktory "C:\xampp\php", rename composer.phar menjadi composer.bat
Perintah: composer --version

atau 

Download: Composer-Setup.exe (Windows)
Install: Install file exe. Composer terinstall di "C:\ProgramData\ComposerSetup\bin" 
         Tambah lokasi direktory dari file binary php ke PATH.
Perintah: jalan "composer --version" di terminal

*/