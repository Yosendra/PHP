Expression, Statement, Block

Expression adalah apapun yang memiliki nilai
Contoh: $a = 5; 5 adalah expression

Statement adalah kalimat lengkap dalam bahasa.
Sebuah statement berisikan execution komplit dan biasanya diakhir dengan titik koma (;).
$name = "John Doe";     // statement 1
echo $name;             // statement 2

Block adalah kumpulan statement yang terdiri dari nol atau lebih statement.
Block diawali dan diakhiri dengan kurung kurawal ({})
{                       // Block
    echo "Start";
    echo "Hello World"
    echo "End";
}

